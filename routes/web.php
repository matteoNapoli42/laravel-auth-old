Route::get('/', function () {
    return view('welcome');
})->middleware('auth');





Route::middleware(['auth', 'verified'])->prefix('admin')->name('admin.')->group(function () {
    Route::get('/', [DashboardController::class, 'index'])->name('dashboard');
});

/*Usiamo il middleware per raggruppare delle rotte. In questo caso appartengono alle pagine relative alla gestioen del profilo. Anche qui necessita della condizione di essere autenticato*/
Route::middleware(['auth', 'verified'])->group(function () {
    Route::get('/profile', [ProfileController::class, 'edit'])->name('profile.edit');
    Route::patch('/profile', [ProfileController::class, 'update'])->name('profile.update');
    Route::delete('/profile', [ProfileController::class, 'destroy'])->name('profile.destroy');
});
//Include una serie di rotte per eseguire le operazioni degli utenti.
